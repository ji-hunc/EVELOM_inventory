  // Ï†ÑÏ≤¥ ÌÉ≠Ïù∏ Í≤ΩÏö∞ Ï†úÌíàÎ≥Ñ+Î∞∞ÏπòÏΩîÎìúÎ≥ÑÎ°ú Í∑∏Î£πÌôîÌï¥ÏÑú Ï¥ùÌï© Í≥ÑÏÇ∞
  const aggregatedInventory = isAllSelected
